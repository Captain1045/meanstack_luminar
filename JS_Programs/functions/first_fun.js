function add(num1,num2)
{
    let result=num1+num2;
    return result;
}
console.log("\nSum = "+add(10,30));

function difference(num1,num2)
{
    let result=num1-num2;
    console.log("\nDifference = "+result);
}
difference(10,30);

function product(num1,num2)
{
    let result=num1*num2;
    console.log("\nProduct = "+result);
}
product(10,30);